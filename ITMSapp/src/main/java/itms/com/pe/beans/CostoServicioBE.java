package itms.com.pe.beans;

public class CostoServicioBE {
	
	private String nombre;
	private int tipo_exa;
	private Double precio;
	
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getTipo_exa() {
		return tipo_exa;
	}
	public void setTipo_exa(int tipo_exa) {
		this.tipo_exa = tipo_exa;
	}
	public Double getPrecio() {
		return precio;
	}
	public void setPrecio(Double precio) {
		this.precio = precio;
	}
	
	
	
	

}
